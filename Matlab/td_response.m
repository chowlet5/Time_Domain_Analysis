%**************************************************************************
% This function is used to compute Modal and total responses              *
% *************************************************************************
function[mr,tr]=td_response(gama,q,n,k,N)
%--------------------------------------------------------------------------
% Modal response
%--------------------------------------------------------------------------
mr = zeros(n,k,N);
for i=1:n
    for j=1:k
        for l=1:N
            mr(i,j,l)=gama(i,j)*q(j,l);
        end
    end
end
%--------------------------------------------------------------------------
% Total response
%--------------------------------------------------------------------------
tr = zeros(n,N);
for i=1:n
    for j=1:k
        for l=1:N
            tr(i,l)= tr(i,l)+ gama(i,j)*q(j,l);
        end
    end
end
    



	
	